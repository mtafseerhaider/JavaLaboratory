public class PrintNulls
{
	public static void main (String[] args)
	{
		Byte bNum = null;
		Long lNum = null;
		Float fNum = null;
		Character cChar = null;

		System.out.println ("Byte value: " + bNum);
		System.out.println ("Long value: " + lNum);
		System.out.println ("Float value: " + fNum);
		System.out.println ("Character value: " + cChar);
	}
}
